# functions for test manipulation 

# function for reversing the string
def reverse_string(string):
    return string[::-1]

#function  for converting all to upper case
def upper_string(string):
    return str.upper(string)

#function  for converting all to lower case
def lower_string(string):
    return str.lower(string)

#function for checking to see if contains any digtis 
def check_alpha(string):
    str.isalpha(string)